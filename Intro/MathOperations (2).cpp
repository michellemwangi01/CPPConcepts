//**C++ MATH**  
#include <iostream>
#include <cmath>//necessary for math operations
using namespace std;

int main()
{
//MAX & MIN FUNCTIONS
cout<<"max: "<<max(5,6)<<endl;
cout<<"min: "<<min(5,6)<<endl;
cout<<"square root: "<<sqrt(64)<<endl;
cout<<"log: "<<log(10)<<endl;
cout<<"round: "<<round(2.5)<<endl;
cout<<"absolute value: "<<abs(-7)<<endl;
cout<<"ceiling value: "<<ceil(-7.2)<<endl;
cout<<"cosine: "<<cos(30)<<endl;
return 0;
}

